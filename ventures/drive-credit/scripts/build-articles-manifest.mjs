#!/usr/bin/env node
/**
 * build-articles-manifest.mjs
 *
 * Scans public/articles/{editorial,affiliate}/* for slugs and writes
 * src/lib/articles-manifest.ts. Runs as `prebuild` so every `next build`
 * picks up new articles automatically — no manual code edits needed
 * when the blog engine drops new HTML into public/articles/.
 *
 * Self-healing invariant: any directory under public/articles/<kind>/
 * that contains an index.html is treated as a live slug.
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..')
const ARTICLES_DIR = path.join(ROOT, 'public', 'articles')
const OUT = path.join(ROOT, 'src', 'lib', 'articles-manifest.ts')

function scan(kind) {
  const dir = path.join(ARTICLES_DIR, kind)
  if (!fs.existsSync(dir)) return []
  const slugs = new Set()
  for (const ent of fs.readdirSync(dir, { withFileTypes: true })) {
    // Affiliate-style: <slug>/index.html
    if (ent.isDirectory()) {
      if (fs.existsSync(path.join(dir, ent.name, 'index.html'))) {
        slugs.add(ent.name)
      }
      continue
    }
    // Editorial-style: <slug>.html flat files
    if (ent.isFile() && ent.name.endsWith('.html')) {
      slugs.add(ent.name.replace(/\.html$/, ''))
    }
  }
  return [...slugs].sort()
}

const editorial = scan('editorial')
const affiliate = scan('affiliate')

const banner =
  '// AUTO-GENERATED by scripts/build-articles-manifest.mjs (runs as prebuild).\n' +
  '// Do not hand-edit. New slugs appear automatically once public/articles/<kind>/<slug>/index.html exists.\n'

const body =
  banner +
  '\n' +
  'export const EDITORIAL_SLUGS: string[] = ' +
  JSON.stringify(editorial, null, 2) +
  '\n\n' +
  'export const AFFILIATE_SLUGS: string[] = ' +
  JSON.stringify(affiliate, null, 2) +
  '\n'

let prev = ''
try { prev = fs.readFileSync(OUT, 'utf8') } catch {}
if (prev !== body) {
  fs.writeFileSync(OUT, body, 'utf8')
  console.log(`[articles-manifest] wrote ${editorial.length} editorial + ${affiliate.length} affiliate slugs → ${path.relative(ROOT, OUT)}`)
} else {
  console.log(`[articles-manifest] up-to-date (${editorial.length} editorial + ${affiliate.length} affiliate)`)
}
